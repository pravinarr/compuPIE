package com.sw.compupie.daoBean;

public class PhysicalHealthItem {
	
	private int id;
	
	private String ICD_CODE;
	
	private String item;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getICD_CODE() {
		return ICD_CODE;
	}

	public void setICD_CODE(String iCD_CODE) {
		ICD_CODE = iCD_CODE;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}
	
	

}
